objectivo(X) :- 
    club(X).

% Definindo a posição do clube na tabela
posicao(X) :- 
    questiona(posicao, X).

% Definindo as vitórias do clube
vitorias(X) :- 
    questiona(vitorias, X).

% Definindo os empates do clube
empates(X) :- 
    questiona(empates, X).

% Definindo as derrotas do clube
derrotas(X) :- 
    questiona(derrotas, X).

% Definindo a quantidade de gols marcados pelo clube
golos_marcados(X) :- 
    questiona(golos_marcados, X).

golos_sofridos(X) :- 
    questiona(golos_sofridos, X).

% Definindo os pontos do clube
pontos(X) :- 
    questiona(pontos, X).

% Definindo o ano de fundação do clube
fundacao(X) :- 
    questiona(fundacao, X).

% Definindo o estádio do clube
estadio(X) :- 
    questiona(estadio, X).

% Definindo a cidade do clube
cidade(X) :- 
    questiona(cidade, X).

% Definindo a capacidade do estádio do clube
capacidade(X) :- 
    questiona(capacidade, X).

% Definindo o nome do clube
club(X) :- 
    posicao(P),
    vitorias(V),
    empates(E),
    derrotas(D),
    golos_marcados(GM),
    golos_sofridos(GS),
    pontos(Po),
    fundacao(F),
    estadio(Es),
    cidade(C),
    capacidade(Ca),
    format('O ge ~w tem a posição ~w, com ~w vitórias, ~w empates, ~w derrotas, ~w gols marcados, ~w gols sofridos, ~w pontos, fundado em ~w, no estádio ~w em ~w, com capacidade de ~w pessoas.', [X, P, V, E, D, GM, GS, Po, F, Es, C, Ca]).

